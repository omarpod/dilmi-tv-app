/// سابقاً كان هذا الملف يحفظ عنوان IP الذي يُدخله المستخدم يدوياً (كنا
/// نستخدمه أثناء التطوير المحلي واختبار الأنفاق مثل Serveo).
///
/// الآن بعد نشر السيرفر بشكل دائم على Render، أصبح الرابط ثابتاً ومعروفاً
/// دائماً، فحذفنا الإدخال اليدوي بالكامل. أبقينا على اسم هذا الملف
/// والدالة `getBaseUrl()` كما هي حتى لا نضطر لتعديل أي كود آخر يستدعيها
/// (مثل api_service.dart)، فقط غيّرنا محتواها لترجع القيمة الثابتة مباشرة.
import '../config/api_config.dart';

class ServerSettings {
  /// يُرجع رابط الـ API الثابت دائماً. أبقيناها async (Future) لتبقى
  /// متوافقة مع الكود القائم في api_service.dart دون أي تعديل هناك.
  static Future<String> getBaseUrl() async {
    return ApiConfig.productionBaseUrl;
  }
}
